#include<iostream>
using namespace std;
int main(){
	//declara a variavel x e inicializa
	//inicializar = inserir um valor inicial para a variavel contadora ou controladora
	int x = 10;
	while(x>=0){ //enquanto (x<=10) faça
		cout<<x<<endl;
		x--;
	}
	int y = 0;
	while(y<=10){ //enquanto (x<=10) faça
		cout<<x<<endl;
		y++;
}
}
